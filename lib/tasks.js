export const TOTAL_WEIGHT = TASKS.reduce((s, t) => s + t.weight, 0); // must be 100

export const TASK_GROUPS = [...new Set(TASKS.map((t) => t.group))];

export function computeScore(taskStatuses) {
  // taskStatuses: { [taskId]: "pending" | "pass" | "fail" | "blocked" }
  return TASKS.reduce((score, task) => {
    const status = taskStatuses[task.id] || "pending";
    return status === "pass" ? score + task.weight : score;
  }, 0);
}

export function groupedTasks() {
  return TASK_GROUPS.map((group) => ({
    group,
    tasks: TASKS.filter((t) => t.group === group),
    totalWeight: TASKS.filter((t) => t.group === group).reduce(
      (s, t) => s + t.weight,
      0
    ),
  }));
}
